package ecole.model;

/**
 * Représente le genre d'une personne.
 * C'est une liste fixe de valeurs possibles.
 */

public enum Genre {
    /** Représente le genre masculin. */
    HOMME,

    /** Représente le genre féminin. */
    FEMME,

    /** Représente une autre identité de genre ou un choix de ne pas spécifier. */
    AUTRE
}